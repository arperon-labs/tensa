//! Report export — generates a structured analytical report from narrative data.
//!
//! Produces a Markdown document with timeline, entity profiles, relationship
//! co-occurrence matrix, and causal chain summaries.

use std::collections::HashMap;

use uuid::Uuid;

use crate::error::Result;
use crate::export::{
    entity_display_name, situation_preview, sort_situations_by_time, NarrativeExport,
};
use crate::types::{Entity, Participation, Role, Situation};

/// Export narrative data as a Markdown analytical report.
pub fn export_report(data: &NarrativeExport) -> Result<String> {
    let mut out =
        String::with_capacity(data.situations.len() * 150 + data.participations.len() * 60 + 1024);

    out.push_str(&format!("# Narrative Report: {}\n\n", data.narrative_id));

    let sorted_situations = sort_situations_by_time(&data.situations);
    let entity_map: HashMap<Uuid, &Entity> = data.entities.iter().map(|e| (e.id, e)).collect();
    let sit_map: HashMap<Uuid, &Situation> = data.situations.iter().map(|s| (s.id, s)).collect();

    // Pre-compute participation indexes (used by both profiles and relationships)
    let mut entity_parts: HashMap<Uuid, Vec<&Participation>> = HashMap::new();
    let mut sit_entities: HashMap<Uuid, Vec<Uuid>> = HashMap::new();
    for p in &data.participations {
        entity_parts.entry(p.entity_id).or_default().push(p);
        sit_entities
            .entry(p.situation_id)
            .or_default()
            .push(p.entity_id);
    }

    out.push_str(&render_timeline(&sorted_situations));
    out.push_str(&render_entity_profiles(&data.entities, &entity_parts));
    out.push_str(&render_relationships(
        &data.entities,
        &sit_entities,
        &entity_map,
    ));
    out.push_str(&render_causal_chains(&data.causal_links, &sit_map));

    out.push_str("---\n*Generated by TENSA*\n");
    Ok(out)
}

/// Render the timeline section as a Markdown table.
fn render_timeline(situations: &[&Situation]) -> String {
    let mut out = String::from("## Timeline\n\n");

    if situations.is_empty() {
        out.push_str("*No events recorded.*\n\n");
        return out;
    }

    out.push_str("| # | Timestamp | Level | Description |\n");
    out.push_str("|---|-----------|-------|-------------|\n");

    for (i, sit) in situations.iter().enumerate() {
        let ts = sit
            .temporal
            .start
            .map(|t| t.format("%Y-%m-%d %H:%M").to_string())
            .unwrap_or_else(|| "unknown".into());
        let level = sit.narrative_level.as_index_str();
        let desc = situation_description(sit);
        out.push_str(&format!("| {} | {} | {} | {} |\n", i + 1, ts, level, desc));
    }
    out.push('\n');
    out
}

/// Render entity profiles with participation counts, roles, and key actions.
fn render_entity_profiles(
    entities: &[Entity],
    entity_parts: &HashMap<Uuid, Vec<&Participation>>,
) -> String {
    let mut out = String::from("## Entity Profiles\n\n");

    if entities.is_empty() {
        out.push_str("*No entities in this narrative.*\n\n");
        return out;
    }

    for entity in entities {
        let name = entity_display_name(entity, "Unknown");
        let etype = format!("{:?}", entity.entity_type);
        out.push_str(&format!("### {} ({})\n\n", name, etype));

        let parts = entity_parts.get(&entity.id);
        let count = parts.map_or(0, |v| v.len());
        out.push_str(&format!("- **Participations:** {}\n", count));

        if let Some(parts) = parts {
            // Role breakdown
            let mut role_counts: HashMap<String, usize> = HashMap::new();
            let mut actions: Vec<&str> = Vec::new();
            for p in parts {
                *role_counts.entry(role_label(&p.role)).or_default() += 1;
                if let Some(ref a) = p.action {
                    if !a.is_empty() {
                        actions.push(a.as_str());
                    }
                }
            }
            let roles: Vec<String> = role_counts
                .iter()
                .map(|(r, c)| format!("{} ({})", r, c))
                .collect();
            if !roles.is_empty() {
                out.push_str(&format!("- **Roles:** {}\n", roles.join(", ")));
            }
            if !actions.is_empty() {
                actions.truncate(5); // top 5
                out.push_str(&format!("- **Key actions:** {}\n", actions.join(", ")));
            }
        }

        out.push_str(&format!("- **Confidence:** {:.2}\n\n", entity.confidence));
    }

    out
}

/// Render relationship co-occurrence matrix.
fn render_relationships(
    entities: &[Entity],
    sit_entities: &HashMap<Uuid, Vec<Uuid>>,
    entity_map: &HashMap<Uuid, &Entity>,
) -> String {
    let mut out = String::from("## Relationships\n\n");

    if entities.len() < 2 {
        out.push_str("*Not enough entities for relationship analysis.*\n\n");
        return out;
    }

    // Count co-occurrences for each entity pair
    let mut cooccurrences: HashMap<(Uuid, Uuid), usize> = HashMap::new();
    for entities_in_sit in sit_entities.values() {
        for (i, &a) in entities_in_sit.iter().enumerate() {
            for &b in &entities_in_sit[i + 1..] {
                let key = if a < b { (a, b) } else { (b, a) };
                *cooccurrences.entry(key).or_default() += 1;
            }
        }
    }

    if cooccurrences.is_empty() {
        out.push_str("*No co-occurrences detected.*\n\n");
        return out;
    }

    // Sort by count descending
    let mut pairs: Vec<_> = cooccurrences.into_iter().collect();
    pairs.sort_by(|a, b| b.1.cmp(&a.1));

    out.push_str("| Entity A | Entity B | Co-occurrences |\n");
    out.push_str("|----------|----------|----------------|\n");

    for ((a, b), count) in &pairs {
        let name_a = entity_map
            .get(a)
            .map_or("?", |e| entity_display_name(e, "?"));
        let name_b = entity_map
            .get(b)
            .map_or("?", |e| entity_display_name(e, "?"));
        out.push_str(&format!("| {} | {} | {} |\n", name_a, name_b, count));
    }
    out.push('\n');
    out
}

/// Render causal chain summary.
fn render_causal_chains(
    causal_links: &[crate::types::CausalLink],
    sit_map: &HashMap<Uuid, &Situation>,
) -> String {
    let mut out = String::from("## Causal Chains\n\n");

    if causal_links.is_empty() {
        out.push_str("*No causal links recorded.*\n\n");
        return out;
    }

    for link in causal_links {
        let from_desc = sit_map
            .get(&link.from_situation)
            .map(|s| situation_description(s))
            .unwrap_or_else(|| link.from_situation.to_string());
        let to_desc = sit_map
            .get(&link.to_situation)
            .map(|s| situation_description(s))
            .unwrap_or_else(|| link.to_situation.to_string());
        out.push_str(&format!(
            "- [{}] --{:?}--> [{}] (strength: {:.2})\n",
            from_desc, link.causal_type, to_desc, link.strength
        ));
    }
    out.push('\n');
    out
}

/// Extract a short description from a situation's raw_content.
fn situation_description(sit: &Situation) -> String {
    situation_preview(sit, 77, || {
        format!("Situation {}", &sit.id.to_string()[..8])
    })
}

/// Display label for a Role.
fn role_label(role: &Role) -> String {
    match role {
        Role::Custom(s) => s.clone(),
        other => format!("{:?}", other),
    }
}

