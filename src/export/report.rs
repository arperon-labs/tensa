    #[test]
    fn test_report_empty_narrative() {
        let data = make_export(vec![], vec![], vec![], vec![]);
        let md = export_report(&data).unwrap();
        assert!(md.contains("# Narrative Report: test-report"));
        assert!(md.contains("No events recorded"));
        assert!(md.contains("No entities"));
    }

    #[test]
    fn test_report_timeline_ordering() {
        let t1 = Utc.with_ymd_and_hms(2024, 1, 1, 12, 0, 0).unwrap();
        let t2 = Utc.with_ymd_and_hms(2024, 6, 1, 12, 0, 0).unwrap();
        let s1 = make_situation_with_time("First", Some(t1));
        let s2 = make_situation_with_time("Second", Some(t2));
        let data = make_export(vec![], vec![s2, s1], vec![], vec![]);
        let md = export_report(&data).unwrap();
        let pos_first = md.find("First").unwrap();
        let pos_second = md.find("Second").unwrap();
        assert!(pos_first < pos_second);
    }

